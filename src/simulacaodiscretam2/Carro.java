package simulacaodiscretam2;

/**
 *
 * @author vinit
 */
public class Carro {
    
    private int TEC;
    private int TS;
    private int entrar_fila;
    private int sair_lavacao;
    private int tempo_fila;
    private int tempo_lavacao;

    /**
     * @return the TEC
     */
    public int getTEC() {
        return TEC;
    }

    /**
     * @param TEC the TEC to set
     */
    public void setTEC(int TEC) {
        this.TEC = TEC;
    }

    /**
     * @return the TS
     */
    public int getTS() {
        return TS;
    }

    /**
     * @param TS the TS to set
     */
    public void setTS(int TS) {
        this.TS = TS;
    }

    /**
     * @return the entrar_fila
     */
    public int getEntrar_fila() {
        return entrar_fila;
    }

    /**
     * @param entrar_fila the entrar_fila to set
     */
    public void setEntrar_fila(int entrar_fila) {
        this.entrar_fila = entrar_fila;
    }

    /**
     * @return the tempo_fila
     */
    public int getTempo_fila() {
        return tempo_fila;
    }

    /**
     * @param tempo_fila the tempo_fila to set
     */
    public void setTempo_fila(int tempo_fila) {
        this.tempo_fila = tempo_fila;
    }

    /**
     * @return the tempo_lavacao
     */
    public int getTempo_lavacao() {
        return tempo_lavacao;
    }

    /**
     * @param tempo_lavacao the tempo_lavacao to set
     */
    public void setTempo_lavacao(int tempo_lavacao) {
        this.tempo_lavacao = tempo_lavacao;
    }

    /**
     * @return the sair_lavacao
     */
    public int getSair_lavacao() {
        return sair_lavacao;
    }

    /**
     * @param sair_lavacao the sair_lavacao to set
     */
    public void setSair_lavacao(int sair_lavacao) {
        this.sair_lavacao = sair_lavacao;
    }
}

